    {
        $keranjangs = Keranjang::where('user_id', auth()->user()->id)->with(['produk'])->get();
        $total = 0;
        foreach ($keranjangs as $keranjang) {
            $total += $keranjang->subtotal;
        }
        return view('cart', [
            'keranjangs' => $keranjangs,
